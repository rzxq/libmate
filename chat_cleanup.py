"""
Авто-очистка чата.

Идея: пока пользователь идёт по одному сценарию (например, добавляет книгу и
отвечает на уточняющие вопросы), все сообщения в рамках этого сценария
запоминаются в "хвост". Как только пользователь нажимает НОВУЮ кнопку
главного меню — весь хвост (сообщения и бота, и пользователя) удаляется,
и в чате остаётся только свежий ответ. Это даёт ощущение единого "экрана
меню", а не бесконечно растущей истории сообщений.

Хранится в памяти процесса (без БД) — это чисто визуальная фича: если бот
перезапустится, максимум что случится — накопленный хвост один раз не
удалится, ничего критичного.

Ограничение Telegram Bot API: сообщение можно удалить, только если ему
меньше 48 часов — это заведомо покрывает сценарий "почистить прошлый шаг
диалога".
"""
from __future__ import annotations

import logging

from aiogram import Bot

logger = logging.getLogger(__name__)

_trails: dict[int, list[int]] = {}


def track(chat_id: int, message_id: int) -> None:
    """Запомнить сообщение как часть текущего 'хвоста' (не удаляем сразу)."""
    _trails.setdefault(chat_id, []).append(message_id)


async def clean(bot: Bot, chat_id: int) -> None:
    """Удалить весь накопленный хвост — вызывать в начале КАЖДОГО пункта
    главного меню, перед тем как показать новый ответ."""
    ids = _trails.pop(chat_id, [])
    for mid in ids:
        try:
            await bot.delete_message(chat_id, mid)
        except Exception:
            # Сообщение уже удалено, старше 48 часов и т.п. — не критично.
            pass
